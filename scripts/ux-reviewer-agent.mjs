// Cerebro · UX Reviewer Agent
//
// Weekly review of key pages. Fetches HTML of public pages, sends to Claude
// for senior product designer feedback. Identifies UX/UI issues and proposes
// concrete fixes with code examples.
//
// Output: docs/ux/YYYY-MM-DD.md committed to repo.
//
// Required env vars:
//   - ANTHROPIC_API_KEY
//   - APP_URL (default https://cerebro-ivory.vercel.app)

import { writeFileSync, mkdirSync } from 'node:fs'
import { dirname } from 'node:path'

const ANTHROPIC_API_KEY = process.env.ANTHROPIC_API_KEY
const APP_URL = process.env.APP_URL || 'https://cerebro-ivory.vercel.app'

if (!ANTHROPIC_API_KEY) {
  console.error('Missing ANTHROPIC_API_KEY')
  process.exit(1)
}

const today = new Date().toISOString().slice(0, 10)

const PAGES_TO_REVIEW = [
  { path: '/', name: 'Landing' },
  { path: '/pricing', name: 'Pricing' },
  { path: '/waitlist', name: 'Waitlist' },
  { path: '/auth', name: 'Auth (login)' },
  { path: '/auth?mode=signup', name: 'Auth (signup)' },
  { path: '/privacy', name: 'Privacy' },
  { path: '/terms', name: 'Terms' },
]

const SYSTEM_PROMPT = `Eres senior product designer revisando un SaaS B2B (Cerebro).
ICP: Heads de Ops/CS/People en LATAM, mid-market 50-200 personas.

Para cada página identificas:
- Issues de jerarquía visual (qué se ve primero, debería?)
- CTAs débiles o ausentes
- Microcopy mejorable (en español natural)
- Inconsistencias de spacing/tipografía/color
- Problemas de accesibilidad (contraste WCAG AA, aria-labels)
- Friction en el flow del usuario
- Cosas que un Head of Ops chileno NO entendería al primer vistazo

Para cada issue propones fix concreto con ejemplo de código (Tailwind/React).

Devuelves markdown estructurado, MUY directo, sin paja.
Prioridades: 🔴 high (afecta conversión) / 🟡 medium / 🟢 nice-to-have`

async function fetchPageHtml(path) {
  const url = `${APP_URL}${path}`
  try {
    const r = await fetch(url, { headers: { 'User-Agent': 'CerebroUXAgent/1.0' } })
    if (!r.ok) return `(failed to fetch: ${r.status})`
    const html = await r.text()
    // Strip <script> tags to focus on visible content
    return html.replace(/<script[\s\S]*?<\/script>/g, '').slice(0, 30000)
  } catch (e) {
    return `(fetch error: ${e.message})`
  }
}

console.log(`🎨 UX Reviewer · ${today}`)

let allReports = `# Cerebro · UX Review — ${today}\n\n`
allReports += `> Revisado: ${PAGES_TO_REVIEW.length} páginas en ${APP_URL}\n\n`
allReports += `---\n\n`

for (const page of PAGES_TO_REVIEW) {
  console.log(`Reviewing ${page.name}...`)
  const html = await fetchPageHtml(page.path)

  const userPrompt = `Página: **${page.name}** (${APP_URL}${page.path})

HTML (truncado a 30K chars):

\`\`\`html
${html}
\`\`\`

Dame top 5 issues priorizados con fixes concretos.`

  const resp = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'x-api-key': ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01',
      'content-type': 'application/json',
    },
    body: JSON.stringify({
      model: 'claude-sonnet-4-5',
      max_tokens: 2048,
      system: SYSTEM_PROMPT,
      messages: [{ role: 'user', content: userPrompt }],
    }),
  })

  if (!resp.ok) {
    console.error(`Failed for ${page.name}:`, await resp.text())
    allReports += `## ${page.name}\n\n_(failed to review)_\n\n`
    continue
  }

  const data = await resp.json()
  const text = data?.content?.[0]?.text || '(no response)'
  allReports += `## ${page.name} (\`${page.path}\`)\n\n${text}\n\n---\n\n`

  await new Promise((r) => setTimeout(r, 1000))
}

allReports += `\n_Generated by UX Reviewer Agent · ${new Date().toISOString()}_\n`

const path = `docs/ux/${today}.md`
mkdirSync(dirname(path), { recursive: true })
writeFileSync(path, allReports)
console.log(`✅ UX review written to ${path}`)
